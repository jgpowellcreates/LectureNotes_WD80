//Equal To
let equalTo = "3" == 3;
console.log(equalTo);

//Strict Equal To    "STRICT" checks for the value AS WELL AS the data type
let strictEqualTo = "3" === 3;
console.log(strictEqualTo);

//Not Equal To
let notEqualTo = "3" != 3;
console.log(notEqualTo);

//Not Strict Equal To
let strictNotEqualTo = "3" !== 3;
console.log(strictNotEqualTo);

//Greater Than
let greaterThan = 4> 3;
console.log(greaterThan);

//Less Than
let lessThan = 33 < 100;
console.log(lessThan);

//Greater Than or Equal To
let greaterThanOrEqualTo = 3 >= 2;
console.log(greaterThanOrEqualTo);

//Less Than or Equal To
let lessThanOrEqualTo = 2 <= 3;
console.log(lessThanOrEqualTo);

//And  &&  - checks if both conditions are true
2 + 2 && 3 + 3

//Or  ||  - checks if one of the conditions is true
2 || 3;